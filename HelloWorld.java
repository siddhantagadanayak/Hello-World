class HelloWorld //The first line defines a class called HelloWorld.
{ // starting of class
  public static void main(String[] args)//public again means that anyone can access it.
                                        //static means that you can run this method without creating an instance of Main.
                                        //void means that this method doesn't return any value.
                                        //main is the name of the method.
  { //starting of main function
    System.out.println("Hello World!!"); //System is a pre-defined class that Java provides us and it holds some useful methods and variables.
                                        //out is a static variable within System that represents the output of your program (stdout).
                                        //println is a method of out that can be used to print a line.
  } // ending of main function.
} // ending of class
